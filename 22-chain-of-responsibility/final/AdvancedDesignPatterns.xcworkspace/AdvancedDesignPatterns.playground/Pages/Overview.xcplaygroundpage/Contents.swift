 1. **Structural patterns** describe how objects are composed to form larger subsystems.
 Examples of structural patterns are:
 
 - [Flyweight](Flyweight)
 - [Composite](Composite)
 - [Coordinator](Coordinator)
 
 2. **Behavioral patterns** describe how objects communicate with each other.
 Examples of behavioral patterns are:
 
 - [Mediator](Mediator)
 - [Command](Command)
 - [Chain Of Responsibility](ChainOfResponsibility)
 
 3. **Creational patterns** instantiate or "create" objects for you. There are fewer creational patterns than structural and behavioral patterns. For examples of creational patterns, see the Foundational and Intermediate Design Patterns sections.
 
